//////////////////////////////////////////////////////////////////////
/// @class SnGate SimNode.h
/// @brief 多入力ゲートの基底クラス
//////////////////////////////////////////////////////////////////////
class SnGate :
  public SimNode
{
protected:

  /// @brief コンストラクタ
  SnGate(const vector<PackedVal*>& inputs);

  /// @brief デストラクタ
  virtual
  ~SnGate();


protected:
  //////////////////////////////////////////////////////////////////////
  // 継承クラスから呼ばれる関数
  //////////////////////////////////////////////////////////////////////

  /// @brief ファンイン数を得る．
  ymuint
  _fanin_num() const;

  /// @brief pos 番めのファンインの値を得る．
  PackedVal
  _fanin_val(ymuint pos) const;


private:
  //////////////////////////////////////////////////////////////////////
  // データメンバ
  //////////////////////////////////////////////////////////////////////

  // 入力数
  ymuint mFaninNum;

  // ファンインの値のポインタの配列
  PackedVal** mFaninVals;

};


